package com.assingnment.for_whileloop;

public class Print_EvenNumbers {

	public static void main(String[] args) 
	{
		int i;
		System.out.println("All Even Numbers Between 121-129");
		
		for(i=121; i<=229;i++) 
		{
			if(i%2==0) 
			{
				System.out.println(i);
			}
		}
		

	}

}
