package com.demo.whileloop;

public class EvenOrOdd {

	public static void main(String[] args)
	{
		int i=0;
		while(i<=50) 
		{
			if(i%2==0)
			{
				System.out.println(i+" = is even");
			}
			else
			{
				System.out.println(i+" = Number is odd");
			}
			
			i++;
		}
		

	}

}
