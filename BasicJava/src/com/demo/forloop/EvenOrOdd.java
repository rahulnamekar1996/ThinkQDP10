package com.demo.forloop;

public class EvenOrOdd {

	public static void main(String[] args) {
		int i;
		for(i=1;i<=50;i++)
		{
			if(i%2==0)
			{
				System.out.println(i+" is even number");
			}
			else 
			{
				System.out.println(i+" is odd number");
			}
			
		}
		

	}

}
