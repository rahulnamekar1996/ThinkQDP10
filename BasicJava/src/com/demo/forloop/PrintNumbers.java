package com.demo.forloop;

public class PrintNumbers {

	public static void main(String[] args) {
		int i;
		for(i=1;i<=100;i++) 
		{
			if(i%5==0)
			{
				System.out .println(i+"= is divisible by 5");
				
			
			}
			
			else 
			
			{
				System.out.println(i+"= is not divisible by 5");
			}
		}
		
		

	}

}
